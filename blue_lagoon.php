<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>1 oz (30 ml) de licor de curaçao azul.</p>
    <p>1 oz (30 ml) de vodka.</p>
    <p>4 oz (120 ml) de limonada o refresco de lima-limón.</p>
    <p>Rodaja de limón o cereza al marrasquino para decorar (opcional).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un vaso alto (collins) con hielo.</p>

    <p>Vierte el licor de curaçao azul en el vaso.</p>

    <p>Añade el vodka.</p>

    <p>Completa con limonada o refresco de lima-limón.</p>

    <p>Remueve suavemente para mezclar los ingredientes.</p>

    <p>Decora con una rodaja de limón o una cereza al marrasquino si lo deseas.</p>

