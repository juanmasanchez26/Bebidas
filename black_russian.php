<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de vodka.</p>
    <p>1 oz (30 ml) de licor de café (como Kahlúa o Tia Maria).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un vaso old-fashioned (vaso corto) con hielo.</p>

    <p>Vierte el vodka sobre el hielo.</p>

    <p>Luego, añade el licor de café sobre el vodka y el hielo.</p>

    <p>Remueve suavemente para mezclar los ingredientes.</p>

