    <h2>Historia</h2>
    <p>El Negroni es un cóctel italiano icónico que tiene sus raíces en el siglo XX. Su historia y origen están
        estrechamente relacionados con la figura del conde Camillo Negroni, quien desempeñó un papel fundamental en la
        creación de esta bebida.
    </p>
    <p>La historia del Negroni se desarrolla en el Caffè Casoni de Florencia, Italia, en el año 1919. El conde Camillo
        Negroni, un cliente habitual del lugar, era conocido por disfrutar del cóctel Americano, que consiste en vermut
        y Campari, una bebida refrescante y ligeramente amarga. Sin embargo, el conde Negroni deseaba una versión más
        fuerte de esta bebida, por lo que solicitó al bartender Fosco Scarselli que hiciera algunas modificaciones.</p>
    <p>Scarselli aceptó la solicitud del conde y, en lugar de usar agua con gas, sustituyó el agua por ginebra,
        agregando así un mayor contenido alcohólico al cóctel. Además, para realzar su sabor, Scarselli incluyó una
        rodaja de naranja en lugar de la típica rodaja de limón. Esta versión mejorada del Americano se convirtió en el
        cóctel preferido del conde Negroni, y con el tiempo, fue nombrado simplemente "Negroni" en su honor.</p>
    <p>El Negroni se popularizó rápidamente en Italia y más allá, convirtiéndose en un cóctel clásico que atraía tanto a
        locales como a visitantes. Con el tiempo, se consolidó como una bebida emblemática del aperitivo italiano y un
        ícono de la coctelería mundial.</p>
    <p>La receta del Negroni ha permanecido prácticamente inalterada desde entonces, y su sabor único, marcado por la
        ginebra, el Campari y el vermut rojo, lo convierte en un cóctel inconfundible y apreciado por los amantes de la
        coctelería en todo el mundo. La rodaja de naranja que adorna el vaso es una característica distintiva que realza
        la experiencia de saborear un Negroni clásico. Este cóctel es un verdadero homenaje a su origen italiano y a la
        creatividad del conde Negroni y el bartender Fosco Scarselli.</p>


