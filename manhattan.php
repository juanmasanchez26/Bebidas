<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>La historia del Manhattan tiene varias teorías sobre su origen. La más ampliamente aceptada es que el cóctel se
        originó en el siglo XIX en el Manhattan Club de Nueva York. La leyenda cuenta que el cóctel fue creado para una
        fiesta que se celebró en honor de Samuel J. Tilden, quien fue gobernador de Nueva York y candidato a la
        presidencia de los Estados Unidos en 1876. El bartender del club, Jerry Thomas, habría creado este cóctel
        especial para el evento.
    </p>
    <p>La receta original del Manhattan incluía whisky de centeno, vermut dulce y unas gotas de amargo. Con el tiempo,
        el whisky de centeno fue reemplazado en muchas versiones por bourbon, aunque ambas variantes son populares y
        aceptadas en la preparación del cóctel.</p>

    <p>La historia y el origen del Manhattan están enraizados en la tradición coctelera de Estados Unidos, y este cóctel
        ha evolucionado a lo largo de los años para incluir variaciones que se adaptan a los gustos de los bebedores
        contemporáneos.</p>

    <p>El Negroni se popularizó rápidamente en Italia y más allá, convirtiéndose en un cóctel clásico que atraía tanto a
        locales como a visitantes. Con el tiempo, se consolidó como una bebida emblemática del aperitivo italiano y un
        ícono de la coctelería mundial.</p>

    <p>La receta clásica del Manhattan combina whisky (ya sea de centeno o bourbon), vermut dulce y amargos, y se sirve
        en un vaso de cóctel o copa de Martini con una cereza al marrasquino como adorno. El equilibrio entre el whisky
        y el vermut da como resultado un cóctel sofisticado y sabroso, que ha mantenido su atractivo a lo largo del
        tiempo y es un favorito en bares de todo el mundo.</p>


</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de whisky de centeno o bourbon.</p>
    <p>1 oz (30 ml) de vermut dulce.</p>
    <p>1 oz (30 ml) de vermut rojo.</p>
    <p>2-3 gotas de amargos (por lo general, amargos de Angostura).</p>
    <p>Cerezas al marrasquino para decorar (opcional).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un vaso mezclador con hielo para enfriarlo.</p>

    <p>Vierte el whisky de centeno o bourbon, el vermut dulce y las gotas de amargos en el vaso mezclador.</p>

    <p>Revuelve suavemente con una cuchara de cóctel durante unos segundos para enfriar y mezclar los ingredientes.</p>

    <p>Cuela la mezcla en un vaso de cóctel o copa de Martini previamente enfriado.</p>

    <p>Opcionalmente, decora con una cereza al marrasquino.</p>

</section>

