<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>1 y 1/2 oz de Campari.</p>
    <p>Jugo de naranja fresco.</p>
    <p>Hielo</p>

    <h3>Pasos</h3>

    <p>Llena un vaso alto con hielo.</p>

    <p>Vierte 1 y 1/2 oz de Campari sobre el hielo.</p>

    <p>Completa el vaso con jugo de naranja fresco.</p>

    <p>Remueve suavemente para mezclar los ingredientes.</p>

    <p>Decora con una rodaja de naranja en el borde del vaso o dentro del cóctel.</p>

