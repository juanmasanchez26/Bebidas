<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de ron negro Gosling's.</p>
    <p>3-4 oz (90-120 ml) de ginger beer Gosling's o de otra marca de tu elección.</p>
    <p>Rodaja de lima para decorar.</p>
    <p>Hielo</p>

    <h3>Pasos</h3>

    <p>Llena un vaso alto con hielo.</p>

    <p>Vierte el ron negro Gosling's sobre el hielo.</p>

    <p>Agrega el ginger beer lentamente para evitar una excesiva mezcla.</p>

    <p>Revuelve suavemente con una cuchara de bar.</p>

    <p>Decora con una rodaja de lima.</p>

