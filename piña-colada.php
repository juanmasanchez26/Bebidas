    <h2>Historia</h2>
    <p>La Piña Colada es un cóctel tropical muy conocido que se originó en Puerto Rico. Aunque la fecha exacta de su
        creación es motivo de debate, se atribuye su invención al bartender Ramón "Monchito" Marrero, quien trabajaba en
        el Beachcomber Bar del Hotel Caribe Hilton en San Juan, Puerto Rico. Marrero supuestamente creó la Piña Colada
        en 1954 como una bebida que capturara el espíritu y los sabores de la isla.</p>
    <p>El Hotel Caribe Hilton afirma ser el lugar de nacimiento de la Piña Colada y, de hecho, ha registrado la receta
        original en el Congreso de Puerto Rico. La Piña Colada se ha convertido en un símbolo de la hospitalidad y la
        cultura de Puerto Rico y es ampliamente disfrutada en todo el mundo.</p>
  


