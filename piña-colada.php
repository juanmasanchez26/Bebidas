<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

<p>2 oz (60 ml) de ron blanco.</p>
<p>3 oz (90 ml) de jugo de piña.</p>
<p>1 oz (30 ml) de crema de coco.</p>
<p>Trozos de piña y una rodaja de piña para decorar (opcional).</p>
<p>Cerezas al marrasquino (opcional).</p>
<p>1 taza de hielo.</p>

<h3>Pasos</h3>

    <p>Agrega el ron blanco, el jugo de piña, la crema de coco y el hielo en una licuadora.</p>

    <p>Licúa hasta que la mezcla esté suave y cremosa.</p>

    <p>Vierte la Piña Colada en un vaso alto o en un vaso de huracán.</p>

    <p>Decora con trozos de piña, una rodaja de piña y una cereza al marrasquino, si lo deseas.</p>



