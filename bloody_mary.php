<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>El origen exacto del Bloody Mary es motivo de debate, pero la versión más aceptada sugiere que se originó en la
        década de 1920. Se le atribuye a Fernand Petiot, un barman francés que trabajaba en el Harry's New York Bar en
        París. Originalmente, la bebida se llamaba "Bucket of Blood" debido a su color rojo y al bar de Chicago con el
        mismo nombre, pero con el tiempo, el nombre se convirtió en Bloody Mary.
    </p>

    <p>La receta original del Bloody Mary consistía en vodka, jugo de tomate, jugo de limón, sal, pimienta, salsa
        Worcestershire y un toque de salsa picante. A medida que la bebida se popularizó en los Estados Unidos, se
        añadieron ingredientes como el apio y los encurtidos, lo que le dio su característica garnitura y sabor.
    </p>

    <p>El cóctel ha tenido una serie de variaciones regionales y nombres alternativos a lo largo de los años. Por
        ejemplo, en el Reino Unido se conoce como "Bloody Maria" cuando se prepara con tequila en lugar de vodka. En el
        sur de los Estados Unidos, se le conoce como "Red Snapper" y se prepara con ginebra en lugar de vodka.
    </p>

    <p>El Bloody Mary ha perdurado como un cóctel popular y es especialmente conocido como una bebida de brunch o
        desayuno. Su versatilidad y capacidad para personalizarlo según los gustos individuales lo han mantenido
        relevante y amado por los amantes de la coctelería en todo el mundo.</p>


</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>1 y 1/2 oz (45 ml) de vodka.</p>
    <p>3 oz (90 ml) de jugo de tomate.</p>
    <p>1/2 oz (15 ml) de jugo de limón fresco.</p>
    <p>1 pizca de sal de apio.</p>
    <p>1 pizca de pimienta negra.</p>
    <p>2-3 gotas de salsa Worcestershire.</p>
    <p>2-3 gotas de salsa picante, como salsa Tabasco (ajusta la cantidad según tu nivel de tolerancia al picante).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un vaso alto o vaso de pinta con hielo.</p>

    <p>Agrega el vodka, el jugo de tomate y el jugo de limón fresco al vaso.</p>

    <p>Añade la pizca de sal de apio, pimienta negra, salsa Worcestershire y salsa picante al gusto.</p>

    <p>Remueve bien para mezclar todos los ingredientes.</p>

    <p>Decora el cóctel con una ramita de apio, una rodaja de limón, aceitunas verdes, pepinillos o la garnitura de tu
        elección.</p>

    <p>Sirve con una pajita o agitador, si lo deseas.</p>

</section>

