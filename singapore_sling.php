<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>1 y 1/2 oz (45 ml) de ginebra.</p>
    <p>1/2 oz (15 ml) de licor de cereza (cherry brandy).</p>
    <p>1/4 oz (7 ml) de Cointreau.</p>
    <p>1/4 oz (7 ml) de Benedictine.</p>
    <p>4 oz (120 ml) de jugo de piña.</p>
    <p>1/2 oz (15 ml) de jugo de limón fresco.</p>
    <p>1 dash de Angostura bitters.</p>
    <p>1/2 oz (15 ml) de granadina.</p>
    <p>Rodaja de piña, cereza al marrasquino y naranja para decorar.</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena una coctelera con hielo.</p>

    <p>Agrega todos los ingredientes, incluyendo la ginebra, el licor de cereza, el Cointreau, el Benedictine, el jugo
        de piña, el jugo de limón, el Angostura bitters y la granadina.</p>

    <p>Agita enérgicamente durante unos 15-20 segundos.</p>

    <p>Cuela la mezcla en una copa larga llena de hielo.</p>

    <p>Decora con una rodaja de piña, una cereza al marrasquino y una rodaja de naranja.</p>

</section>

