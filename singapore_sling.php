    <h2>Historia</h2>
    <p>El Singapore Sling fue creado por Ngiam Tong Boon, un talentoso bárman del Raffles Hotel en Singapur, a
        principios del siglo XX. El cóctel se originó en el Long Bar del hotel y se convirtió en una bebida emblemática
        de la ciudad.
    </p>

    <p>La creación del Singapore Sling coincidió con la "Era Dorada de la Coctelería", un período en el que los cócteles
        se volvieron populares en todo el mundo. La gente comenzó a experimentar con mezclas sofisticadas y exóticas
    </p>

    <p>El Singapore Sling fue diseñado para reflejar la influencia tropical de la región y se caracteriza por su sabor
        afrutado y su color rosado. La presentación en una copa larga y la decoración con frutas y sombrillas de cóctel
        añaden un toque de exotismo.
    </p>

    <p>El cóctel original del Raffles Hotel se preparaba con ingredientes frescos, como ginebra, licor de cereza,
        Cointreau, jugo de piña y limón, y otros componentes aromáticos y amargos que le daban profundidad de sabor.</p>

    <p>El Singapore Sling ha experimentado un renacimiento en la coctelería moderna, y los bármanes a menudo presentan
        versiones contemporáneas de este clásico.</p>


