<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>En 1931, Giuseppe Cipriani fundó Harry's Bar en Venecia. Este bar se convirtió en un ícono no solo por su
        innovadora coctelería, sino también por su elegancia y su rol como punto de encuentro de la alta sociedad y
        celebridades de la época. Cipriani, un astuto empresario y amante de la gastronomía, se propuso crear un cóctel
        que no solo deleitara el paladar, sino que también rindiera homenaje a la riqueza artística y cultural de
        Venecia.
    </p>

    <p>El nombre del cóctel, Bellini, rinde homenaje al pintor renacentista veneciano Giovanni Bellini. Cipriani se
        inspiró en la paleta de colores suaves y rosados que Bellini utilizó en sus obras maestras. Decidió crear un
        cóctel que reflejara los tonos y la elegancia que se encuentran en los cuadros de Bellini. Es así como nació el
        Bellini, un cóctel que lleva el nombre de un maestro de la pintura y la creatividad artística de Venecia.
    </p>

    <p>El Bellini es un cóctel de temporada, ya que su ingrediente principal, el durazno blanco o melocotón blanco, solo
        está disponible durante el verano. La frescura y la calidad de los duraznos son fundamentales para el sabor y la
        belleza del cóctel. El puré de durazno blanco es la esencia que imparte el distintivo color rosado y el sabor
        aterciopelado al Bellini.</p>



</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de puré de durazno blanco o melocotón blanco (hecho con duraznos frescos o congelados).</p>
    <p>4 oz (120 ml) de vino espumoso (preferiblemente prosecco).</p>
    <p>Azúcar (opcional, para ajustar la dulzura).</p>
    <p>Durazno blanco o melocotón blanco en rodajas para decorar (opcional).</p>
    <p>Tira de cáscara de limón para decorar.</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Prepara el puré de durazno blanco o melocotón blanco. Puedes pelar y triturar duraznos frescos o usar duraznos
        congelados.</p>

    <p>Si deseas, endulza el puré con azúcar según tu preferencia.</p>

    <p>En una copa de flauta o copa de cóctel, vierte el puré de durazno.</p>

    <p>Lenta y suavemente, añade el vino espumoso (prosecco) para mezclarlo con el puré de durazno. Puedes agregar hielo
        si lo prefieres.</p>

    <p>Revuelve ligeramente para mezclar los ingredientes.</p>

    <p>Opcionalmente, decora con una rodaja de durazno blanco o melocotón blanco.</p>

</section>

