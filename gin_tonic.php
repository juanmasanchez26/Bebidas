<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>
        La historia del Gin Tonic tiene sus orígenes en el uso de la quinina, una sustancia que se extrae de la corteza
        del árbol de la quina. Durante el siglo XIX, cuando el Imperio Británico tenía colonias en regiones tropicales,
        la quinina se utilizaba para combatir la malaria, una enfermedad transmitida por mosquitos.
    </p>

    <p>Sin embargo, la quinina sola tenía un sabor extremadamente amargo, lo que dificultaba su consumo. Los oficiales
        británicos en la India comenzaron a mezclar la quinina con agua, azúcar, limón y ginebra para hacerla más
        agradable al paladar. La mezcla se convirtió en una bebida popular entre los colonizadores británicos como una
        forma refrescante y medicinal de consumir la quinina.</p>

    <p>Con el tiempo, la mezcla se perfeccionó y evolucionó en lo que hoy conocemos como Gin Tonic. La adición de hielo
        y otros elementos decorativos, como rodajas de limón, lima o bayas de enebro, contribuyó a la presentación
        estética del cóctel.</p>

    <p>Durante el siglo XX, el Gin Tonic experimentó varias etapas de popularidad, desde ser una bebida clásica hasta
        caer en desuso en ciertos periodos. Sin embargo, a partir de la década de 2000, el Gin Tonic experimentó un
        renacimiento en la escena de los cócteles, convirtiéndose en una opción de bebida de moda. La explosión de la
        escena de la ginebra artesanal y la creatividad en la mezcla de cócteles contribuyeron a su resurgimiento.
    </p>

    <p>Hoy en día, el Gin Tonic es un clásico atemporal y sigue siendo una opción popular en bares y restaurantes de
        todo el mundo. La diversidad de ginebras disponibles, así como la variedad de aguas tónicas y los diferentes
        elementos decorativos, permiten a los amantes de los cócteles personalizar su experiencia con esta refrescante
        bebida.</p>




</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de ginebra.</p>
    <p>Agua tónica.</p>
    <p>Rodaja de limón o lima (opcional, para decorar).</p>
    <p>Bayas de enebro (opcional, para decorar).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un vaso de balón con cubitos de hielo. Este tipo de vaso es ideal para apreciar los aromas
        del Gin Tonic.</p>

    <p>Vierte 50 ml de ginebra sobre el hielo. Puedes ajustar la cantidad de ginebra según tu preferencia de fuerza.</p>

    <p>Agrega agua tónica al vaso. La cantidad dependerá de tu gusto personal, pero generalmente se utiliza una
        proporción de 1:2 o 1:3 de ginebra a agua tónica.</p>

    <p>Remueve suavemente con una cuchara larga para mezclar los ingredientes.</p>

    <p>Opcionalmente, decora tu Gin Tonic con una rodaja de limón o lima. También puedes añadir bayas de enebro para
        realzar los sabores de la ginebra.</p>

</section>

