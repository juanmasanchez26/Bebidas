<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de tequila.</p>
    <p>1 oz (30 ml) de jugo de pomelo.</p>
    <p>Refresco de toronja (como Squirt o Jarritos).</p>
    <p>Jugo de limón (opcional).</p>
    <p>Sal para escarchar el borde del vaso (opcional).</p>
    <p>Rodaja de pomelo para decorar.</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Si lo deseas, puedes escarchar el borde de un vaso alto con sal.</p>

    <p>Llena el vaso con hielo.</p>

    <p>Vierte el tequila y el jugo de pomelo en el vaso y revuelve suavemente.</p>

    <p>Completa el vaso con refresco de toronja (agrega tanto como desees, dependiendo de tu preferencia por la dulzura
        y el nivel de carbonatación).</p>

    <p>Si lo deseas, añade un toque de jugo de limón para realzar el sabor cítrico.</p>

    <p>Decora con una rodaja de pomelo en el borde del vaso.</p>

