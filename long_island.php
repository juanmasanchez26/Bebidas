<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>El Long Island Iced Tea se originó en la década de 1970, un período en el que la coctelería estaba experimentando
        un renacimiento y la búsqueda de bebidas innovadoras era común. Se cree que el cóctel fue creado por varios
        bármanes en diferentes lugares de los Estados Unidos alrededor de la misma época.
    </p>

    <p>El nombre "Iced Tea" (Té Helado) se debe a que el cóctel tiene un parecido sorprendente con el té helado en
        cuanto a color y sabor, a pesar de no contener ninguna gota de té en su preparación. Esta similitud fue
        intencional y se buscaba sorprender a los bebedores.
    </p>

    <p>El Long Island Iced Tea es conocido por su combinación de varios tipos de licor, que incluyen vodka, ron blanco,
        ginebra, tequila, triple sec y sour mix (mezcla agridulce). A pesar de la variedad de licores, el cóctel logra
        un equilibrio sorprendente.</p>

    <p>El Long Island Iced Tea se volvió muy popular y ha mantenido su atractivo a lo largo de las décadas. A pesar de
        su potencia, su sabor refrescante lo convierte en una elección popular en bares y restaurantes.
    </p>




</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>1/2 oz (15 ml) de vodka.</p>
    <p>1/2 oz (15 ml) de ron blanco.</p>
    <p>1/2 oz (15 ml) de ginebra.</p>
    <p>1/2 oz (15 ml) de tequila.</p>
    <p>1/2 oz (15 ml) de triple sec (licor de naranja).</p>
    <p>1 oz (30 ml) de jugo de limón fresco.</p>
    <p>1 oz (30 ml) de sour mix (mezcla agridulce).</p>
    <p>Cola (refresco de cola) al gusto.</p>
    <p>Rodaja de limón o lima para decorar (opcional).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un vaso alto (collins) con hielo.</p>

    <p>Vierte el vodka, el ron, la ginebra, el tequila, el triple sec, el jugo de limón y el sour mix en el vaso.</p>

    <p>Remueve suavemente para mezclar los ingredientes.</p>

    <p>Completa con cola al gusto.</p>

    <p>Decora con una rodaja de limón o lima si lo deseas.</p>

</section>

