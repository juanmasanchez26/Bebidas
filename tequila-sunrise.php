<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>Aunque el origen exacto del Tequila Sunrise es motivo de debate, una de las historias más aceptadas es que se
        creó en Tijuana, México, en el famoso bar Agua Caliente durante la década de 1930. Durante la época de la
        Prohibición en los Estados Unidos, Tijuana se convirtió en un destino popular para aquellos que buscaban bebidas
        alcohólicas. En ese contexto, se dice que el Tequila Sunrise fue inventado por el bartender Carlos "Danny"
        Herrera para atraer a los clientes estadounidenses.
    </p>
    <p>El Tequila Sunrise original consistía en tequila, jugo de limón fresco y granadina, lo que le daba su
        característico aspecto degradado, con los colores del amanecer. La popularidad del cóctel creció a lo largo de
        las décadas y se convirtió en una bebida emblemática en los años 70 y 80. Fue ampliamente adoptado y adaptado en
        bares de todo el mundo.</p>

    <p>El nombre "Tequila Sunrise" hace referencia a los colores del amanecer, con el rojo y el naranja representando el
        sol que aparece en el horizonte. La presentación visual del cóctel, con sus colores estratificados, lo convirtió
        en un cóctel atractivo tanto para los amantes de la coctelería como para los aficionados.</p>

   

</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de tequila.</p>
    <p>4 oz (120 ml) de jugo de naranja.</p>
    <p>1 oz (30 ml) de granadina.</p>
    <p>Rodaja de naranja y cereza al marrasquino para decorar (opcional).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un vaso alto con hielo.</p>

    <p>Vierte el tequila y el jugo de naranja en el vaso y revuelve suavemente.</p>

    <p>Con la parte posterior de una cuchara, vierte lentamente la granadina a lo largo del borde del vaso para que descienda y cree el aspecto degradado característico.</p>

    <p>Opcionalmente, decora con una rodaja de naranja y una cereza al marrasquino en el borde del vaso.</p>

</section>

