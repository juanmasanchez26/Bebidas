<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>El Gimlet se originó en la Armada Real británica a mediados del siglo XIX. Los oficiales de la marina
        descubrieron que el jugo de limón o lima era una forma efectiva de prevenir el escorbuto entre los marineros,
        gracias a su alto contenido de vitamina C. Para hacerlo más agradable, comenzaron a mezclar el jugo de limón con
        ginebra, lo que dio lugar al cóctel Gimlet. La bebida se convirtió en una forma popular de consumir ginebra y,
        al mismo tiempo, mantenerse saludable en alta mar.
    </p>

    <p>El nombre "Gimlet" se asocia a menudo con Sir Thomas Desmond Gimlette, un cirujano de la marina británica que, a
        fines del siglo XIX, supuestamente promovió esta bebida para prevenir el escorbuto en la tripulación. Sin
        embargo, existen diversas teorías sobre el verdadero origen del nombre. Otra teoría sugiere que el término
        "gimlet" se deriva de una herramienta utilizada para perforar barriles de licor en la Armada Real.
    </p>

    <p>Con el tiempo, el Gimlet se convirtió en un cóctel apreciado en el mundo de la coctelería. La receta original
        incluía ginebra y jugo de lima. A medida que la coctelería evolucionó, el jugo de lima fue reemplazado a menudo
        por cordial de lima o jarabe de lima. Actualmente, el cóctel Gimlet se prepara con ginebra y jarabe de lima en
        la mayoría de las recetas.
    </p>

    <p>El Gimlet se ha mantenido como un clásico en la coctelería y es conocido por su simplicidad y sabor refrescante.
        Es un cóctel apreciado por su historia y su agradable equilibrio entre la ginebra y el sabor cítrico del limón o
        la lima.</p>


</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de ginebra.</p>
    <p>3/4 oz (22 ml) de jarabe de lima o jugo de lima fresco.</p>
    <p>Twist de limón o rodaja de lima para decorar (opcional).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena una coctelera con hielo.</p>

    <p>Agrega la ginebra y el jarabe de lima (o jugo de lima) en la coctelera.</p>

    <p>Agita enérgicamente durante unos 15-20 segundos para enfriar y mezclar los ingredientes.</p>

    <p>Cuela la mezcla en una copa de cóctel previamente enfriada.</p>

    <p>Decora con un twist de limón o una rodaja de lima, si lo deseas.</p>

</section>

