<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de bourbon.</p>
    <p>1 oz (30 ml) de licor de café.</p>
    <p>1/2 oz (15 ml) de jarabe de arce.</p>
    <p>2 gotas de angostura bitters.</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>En un vaso mezclador, combina el bourbon, el licor de café y el jarabe de arce.</p>

    <p>Añade dos gotas de angostura bitters.</p>

    <p>Agita vigorosamente la mezcla con hielo durante unos 15-20 segundos.</p>

    <p>Cuela la mezcla en un vaso bajo con cubos de hielo.</p>

    <p>Opcionalmente, decora con una cereza maraschino para realzar la presentación.</p>

