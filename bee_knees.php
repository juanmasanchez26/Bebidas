<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>El Bee's Knees se originó durante la Prohibición, un período en el que la venta y el consumo de alcohol estaban
        prohibidos en los Estados Unidos. Durante esta época, la calidad del alcohol de contrabando no siempre era la
        mejor, por lo que las personas buscaban formas de mejorar su sabor y ocultar el sabor desagradable.
    </p>

    <p>La receta del Bee's Knees se creó para enmascarar el sabor del alcohol de contrabando que se utilizaba en los
        cócteles de la época. La combinación de gin, miel y jugo de limón no solo endulzaba y suavizaba el sabor del
        alcohol, sino que también agregaba un toque de cítricos y un agradable dulzor gracias a la miel.
    </p>

    <p>El nombre proviene de la jerga de la década de 1920, que se utilizaba para describir algo como lo
        mejor o excelente. La expresión "the bee's knees" (las rodillas de la abeja) se utilizaba para referirse a algo
        que era realmente genial. En este contexto, se aplicó al cóctel debido a su agradable sabor y equilibrio.
    </p>

    <p>El Bee's Knees ha experimentado un resurgimiento en la coctelería moderna y se ha convertido en un cóctel
        clásico. Su perfil de sabor equilibrado y su simplicidad lo hacen atractivo tanto para aficionados como para
        bármanes creativos que buscan darle un giro a los cócteles clásicos.
    </p>


</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de gin.</p>
    <p>3/4 oz (22 ml) de jugo de limón fresco.</p>
    <p>1/2 oz (15 ml) de miel líquida.</p>
    <p>Hielo</p>

    <h3>Pasos</h3>

    <p>Llena una coctelera con hielo.</p>

    <p>Agrega el gin, el jugo de limón fresco y la miel líquida a la coctelera.</p>

    <p>Agita enérgicamente durante unos 15-20 segundos para mezclar y enfriar los ingredientes.</p>

    <p>Cuela la mezcla en una copa de cóctel previamente enfriada.</p>

    <p>Puedes decorar con una rodaja de limón o una espiral de cáscara de limón.</p>

</section>

