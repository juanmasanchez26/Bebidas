<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>1 y 1/2 oz (45 ml) de ginebra.</p>
    <p>1/2 oz (15 ml) licor de frambuesa.</p>
    <p>1 oz (30 ml) de zumo de arándano.</p>
    <p>1/2 oz (15 ml) de licor de durazno.</p>
    <p>1/2 oz (15 ml) de crema de coco</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena una coctelera con cubitos de hielo.</p>

    <p>Vierte ginebra en la coctelera.</p>

    <p>Agrega licor de frambuesa.</p>

    <p>Añade zumo de arándano a la mezcla.</p>

    <p>Incorpora licor de durazno para darle un toque afrutado.</p>

    <p>Agrega crema de coco para suavizar la mezcla y darle una textura cremosa.</p>

    <p>Agita enérgicamente la coctelera para mezclar todos los ingredientes.</p>

    <p>Cuela la mezcla en una copa de cóctel previamente enfriada.</p>

    <p>Decora con una rodaja de limón o una cereza al borde del vaso.</p>

