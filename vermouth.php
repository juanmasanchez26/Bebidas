<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>El vermut, a menudo escrito como "vermouth" en inglés, es un vino fortificado y aromatizado con hierbas, especias
        y otros sabores botánicos. Su historia se remonta a la antigüedad, y su creación se atribuye a civilizaciones
        como los antiguos egipcios y griegos, que preparaban vinos aromatizados con hierbas medicinales. El término
        "vermut" proviene del antiguo griego "wermut", que significa "ajenjo", una de las hierbas comunes utilizadas en
        la elaboración del vermut.</p>
    <p>Sin embargo, la forma moderna del vermut, tal como la conocemos hoy, se desarrolló en el siglo XIX en Italia y
        Francia. El vermut italiano y el vermut francés se volvieron ampliamente populares y se produjeron en varias
        marcas y estilos. El vermut se convirtió en un ingrediente esencial en cócteles clásicos como el Martini y el
        Manhattan.</p>



</section>
