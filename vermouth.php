    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

<p>Vermut (blanco o rojo, según tus preferencias).</p>
<p>Rodaja de cítrico (naranja o limón) o aceituna para decorar (opcional)..</p>
<p>Hielo.</p>

<h3>Pasos</h3>

    <p>Llena una copa de vino o una copa de vermut con hielo.</p>

    <p>Vierte el vermut sobre el hielo. La cantidad de vermut depende de tus preferencias personales, pero generalmente
        se sirve en una proporción de 2:1 con el hielo.</p>

    <p>Revuelve suavemente para enfriar el vermut.</p>

    <p>Decora con una rodaja de cítrico (naranja o limón) o una aceituna, si lo deseas.</p>



