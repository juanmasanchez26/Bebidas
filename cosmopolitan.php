<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>1 y 1/2 oz (45 ml) de vodka.</p>
    <p>1 oz (30 ml) de licor de naranja (como Cointreau o Triple Sec).</p>
    <p>1/2 oz (15 ml) de jugo de lima fresco.</p>
    <p>1 oz (30 ml) de jugo de arándano.</p>
    <p>Cáscara de lima o una rodaja de lima para decorar.</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un agitador de cócteles con hielo para enfriarlo.</p>

    <p>Vierte el vodka, el licor de naranja, el jugo de lima y el jugo de arándano en el agitador.</p>

    <p>Agita enérgicamente durante unos 10-15 segundos para mezclar los ingredientes y enfriar la mezcla.</p>

    <p>Cuela la mezcla en una copa de cóctel previamente enfriada.</p>

    <p>Decora el cóctel con una cáscara de lima o una rodaja de lima en el borde del vaso.</p>

