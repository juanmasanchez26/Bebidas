<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>El Sazerac se originó en la década de 1830 en Nueva Orleans, Louisiana. Es considerado uno de los cócteles más
        antiguos y emblemáticos de la ciudad.
    </p>

    <p>Se dice que el Sazerac es uno de los primeros cócteles que se crearon en los Estados Unidos. En su forma
        original, se preparaba con brandy y se consideraba un aperitivo popular en la ciudad.
    </p>

    <p>El nombre del cóctel proviene del "Sazerac de Forge et Fils", una marca de coñac que se utilizaba como
        ingrediente principal en la receta original. Con el tiempo, el coñac fue reemplazado por whisky, y el cóctel
        evolucionó.
    </p>

    <p>Durante la Prohibición en los Estados Unidos (1920-1933), el Sazerac ganó popularidad al ser servido en una
        versión más "legal" en la ciudad de Nueva Orleans. El uso de bitters de Peychaud, una preparación casera creada
        por Antoine Amédée Peychaud en la década de 1830, se convirtió en un ingrediente esencial en el cóctel.</p>

    <p>El Sazerac se ha convertido en un símbolo de Nueva Orleans y es apreciado por los amantes de los cócteles
        clásicos en todo el mundo.</p>


</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de whisky (tradicionalmente rye whiskey).</p>
    <p>1 cubo de azúcar.</p>
    <p>2 dashes de bitters de Peychaud.</p>
    <p>Absenta o licor de anís.</p>
    <p>Twist de limón.</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Enfriar un vaso Old Fashioned o un vaso de coctelera añadiendo hielo y dejándolo reposar.</p>

    <p>En otro vaso, coloca el azúcar y los bitters de Peychaud. Agita para disolver el azúcar.</p>

    <p>Añade el whisky y revuelve con hielo hasta que esté bien frío.</p>

    <p>Desecha el hielo del primer vaso y añade un chorrito de absenta o licor de anís, girando para cubrir bien las
        paredes del vaso.</p>

    <p>Cuela la mezcla de whisky, azúcar y bitters en el vaso preparado.</p>

    <p>Exprime un twist de limón sobre el cóctel y deséchalo o utilízalo para decorar.</p>

</section>

