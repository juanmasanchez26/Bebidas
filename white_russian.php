    <h2>Historia</h2>
    <p>El White Russian se originó en una época en la que las bebidas con crema estaban en boga. Durante las décadas de
        1950 y 1960, las bebidas que incluían crema eran una elección popular en bares y salones, y el White Russian se
        sumó a esta tendencia.
    </p>

    <p>El White Russian es una variante del Black Russian, otro cóctel clásico. El Black Russian combina vodka y licor
        de café (como el Kahlúa) y es conocido por su sabor robusto. La adición de crema al Black Russian resultó en la
        creación del White Russian, que es más suave y aterciopelado.
    </p>

    <p>El White Russian se popularizó aún más gracias a la película "The Big Lebowski" (1998), donde el personaje
        principal, "The Dude", interpretado por Jeff Bridges, es un aficionado a esta bebida. La película elevó la
        notoriedad del White Russian y lo convirtió en un cóctel de culto.</p>




</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de vodka.</p>
    <p>1 oz (30 ml) de licor de café (como Kahlúa).</p>
    <p>1 oz (30 ml) de crema (nata líquida).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un vaso old-fashioned (vaso corto) con hielo.</p>

    <p>Vierte el vodka y el licor de café sobre el hielo y revuelve suavemente.</p>

    <p>Con una cuchara larga, vierte lentamente la crema sobre la parte superior de la bebida para crear una capa
        flotante.</p>

    <p>No revuelvas la crema; la idea es que forme una capa distintiva en la parte superior del cóctel.</p>

