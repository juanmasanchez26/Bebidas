<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>El Old-Fashioned es uno de los cócteles más clásicos y antiguos que se conocen. Su origen se remonta al siglo
        XIX, y es considerado uno de los cócteles originales. A medida que los cócteles se volvían más complejos con la
        incorporación de frutas, jarabes y licores varios, algunos puristas comenzaron a pedir su bebida "a la antigua
        manera" o "old-fashioned". Así nació el nombre de este icónico cóctel.</p>
    <p>La receta del Old-Fashioned se mencionó por primera vez en el libro de cócteles "The Balance and Columbian
        Repository" en 1806, y se llamó "cocktail" en ese momento. A lo largo del tiempo, el cóctel fue simplificado y
        se convirtió en el Old-Fashioned que conocemos hoy en día.</p>



</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

<p>2 oz (60 ml) de bourbon o ron (algunas variantes utilizan whisky escocés o brandy).</p>
<p>1 terrón de azúcar.</p>
<p>2-3 gotas de amargo de Angostura.</p>
<p>Rodaja de naranja o cáscara de naranja para decorar (opcional).</p>
<p>Cereza al marrasquino para decorar (opcional).</p>
<p>Hielo.</p>

<h3>Pasos</h3>

    <p>Coloca el terrón de azúcar en el fondo de un vaso Old-Fashioned.</p>

    <p>Añade 2-3 gotas de amargo de Angostura al azúcar. Puedes ajustar la cantidad según tu preferencia de amargor.</p>

    <p>Agrega unas gotas de agua y remueve para disolver el azúcar y los amargos.</p>

    <p>Llena el vaso con hielo.</p>

    <p>Vierte el bourbon (o ron tambien algunas variantes utilizan whisky escocés o brandy) sobre el hielo.</p>

    <p>Remueve suavemente durante unos segundos para enfriar y mezclar los ingredientes.</p>

    <p>Decora con una rodaja de naranja o una cáscara de naranja, y opcionalmente con una cereza al marrasquino.</p>

</section>

