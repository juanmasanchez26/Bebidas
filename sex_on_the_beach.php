<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>1 y 1/2 oz (45 ml) de vodka.</p>
    <p>1/2 oz (15 ml) de licor de durazno (schnapps de durazno).</p>
    <p>2 oz (60 ml) de jugo de arándano.</p>
    <p>2 oz (60 ml) de jugo de naranja.</p>
    <p>Rodaja de naranja y/o cereza al marrasquino para decorar (opcional).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un vaso alto (collins) con hielo.</p>

    <p>Vierte el vodka y el licor de durazno en el vaso.</p>

    <p>Agrega el jugo de arándano y el jugo de naranja.</p>

    <p>Remueve suavemente para mezclar los ingredientes.</p>

    <p>Decora con una rodaja de naranja y/o una cereza al marrasquino si lo deseas.</p>

