<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>El Pink Gin se originó en la Armada Británica durante el siglo XIX. Se dice que era una bebida popular entre los
        oficiales navales, especialmente aquellos que servían en climas tropicales y se veían afectados por el calor y
        la humedad.
    </p>

    <p>Originalmente, el Pink Gin se creó como un remedio para el mareo y las náuseas que sufrían los marineros. La
        ginebra se consideraba útil para aliviar estos síntomas, y los bíteres aromáticos se añadían para hacerla más
        agradable y efectiva.
    </p>

    <p>El Pink Gin es un cóctel extremadamente simple que consta de solo dos ingredientes principales: ginebra y bíteres
        aromáticos (como Angostura bitters). La clave está en la cantidad y la preparación adecuada.
    </p>

    <p>El nombre "Pink Gin" proviene de la coloración rosa pálida que adquiere el cóctel debido a la incorporación de
        los bíteres aromáticos. El color característico es parte de su atractivo.</p>

    <p>A pesar de su simplicidad, el Pink Gin ha mantenido su popularidad y es apreciado por aquellos que buscan un
        cóctel clásico con un sabor distintivo y un toque de amargura.</p>


</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de ginebra.</p>
    <p>2-3 dashes de bíteres aromáticos (Angostura bitters).</p>
    <p>Twist de limón o limón para decorar (opcional).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un vaso Old Fashioned con hielo para enfriarlo.</p>

    <p>Agrega los bíteres aromáticos y gira el vaso suavemente para que se impregnen en el hielo.</p>

    <p>Desecha cualquier exceso de bíteres que haya quedado en el vaso.</p>

    <p>Vierte la ginebra en el vaso con hielo y los bíteres aromáticos.</p>

    <p>Remueve suavemente para mezclar los ingredientes.</p>

    <p>Decora con un twist de limón o una rodaja de limón si lo deseas.</p>

</section>

