    <h2>Historia</h2>
    <p>El Boulevardier es una variante del Negroni, un cóctel que combina gin, vermut y Campari. La historia del
        Boulevardier comienza cuando un escritor estadounidense llamado Erskine Gwynne, quien vivía en París, decidió
        poner su propia versión del Negroni en la revista que editaba, "Boulevardier", en la década de 1920. Gwynne
        sustituyó la ginebra del Negroni por whisky, creando así una nueva bebida.
    </p>

    <p>La década de 1920 en París fue una época de efervescencia cultural y creatividad, con numerosos bares y cafés
        frecuentados por artistas, escritores y expatriados estadounidenses. El Boulevardier se convirtió en un cóctel
        popular en estos círculos bohemios y literarios. Su nombre, derivado de la revista de Gwynne, reflejaba la vida
        en el bullicioso Boulevard de la capital francesa.
    </p>

    <p>Es conocido por su equilibrio entre el sabor agridulce y amargo del Campari, el dulzor del vermut
        y la robustez del whisky. A lo largo de los años, ha mantenido su popularidad gracias a su perfil de sabor
        distintivo y su versatilidad para adaptarse a diferentes tipos de whisky y vermut.
    </p>

    <p>El Boulevardier ha experimentado un resurgimiento en la coctelería moderna, junto con otros cócteles clásicos. Su
        perfil de sabor único lo hace atractivo tanto para aficionados a la coctelería como para bármanes creativos que
        experimentan con ingredientes y variantes.

    </p>


