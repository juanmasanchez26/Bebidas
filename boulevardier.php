<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>1 y 1/2 oz (45 ml) de whisky (generalmente bourbon o rye).</p>
    <p>3/4 oz (22 ml) de vermut rojo.</p>
    <p>3/4 oz (22 ml) de Campari.</p>
    <p>Twist de naranja o cereza para decorar.</p>
    <p>Hielo</p>

    <h3>Pasos</h3>

    <p>Llena un vaso mezclador con hielo.</p>

    <p>Vierte el whisky, el vermut rojo y el Campari en el vaso mezclador.</p>

    <p>Remueve suavemente durante unos 15-20 segundos para mezclar y enfriar los ingredientes.</p>

    <p>Cuela la mezcla en una copa de cóctel previamente enfriada.</p>

    <p>Decora con un twist de naranja o una cereza al marrasquino, según tu preferencia.</p>
    
</section>

