<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>La caipirinha es un cóctel brasileño que se ha convertido en una bebida
        emblemática de Brasil. Su origen se encuentra en las zonas rurales de
        Brasil, donde los trabajadores de las plantaciones de caña de azúcar
        solían consumir una bebida llamada "cachaça", que es un aguardiente
        de caña de azúcar. Con el tiempo, la cachaça se mezcló con limón,
        azúcar y hielo, lo que dio lugar a la creación de la caipirinha.</p>
    <p>El nombre de Caipirinha fue puesto alrededor del 1900, y viene
        de una mezcla de “caipira” (término que hace
        referencia a los campesinos que habitaban en los
        bosques) y de “Curupirinha” (Curupira era un
        demonio místico que vivía en los bosques, cuyo
        diminutivo es “curupirinha”; además, esta palabra se
        usa para referirse al estado de ebriedad en que se
        comienza a tener visión borrosa)</p>
    <p>La caipirinha se popularizó en todo Brasil y, posteriormente,
        en el mundo. Hoy en día, es un cóctel ampliamente reconocido
        y disfrutado en bares y restaurantes de todo el planeta.</p>

</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>

    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de cachaça (aguardiente de caña de azúcar).</p>
    <p>1 lima.</p>
    <p>2 cucharaditas de azúcar (ajusta según tu preferencia).</p>
    <p>Hielo.</p>
    <p>Rodajas de lima y una rodaja fina de lima para decorar.</p>

    <h3>Pasos</h3>
    <p>Lava y corta los extremos de la lima. Luego, córtala
        en cuartos y quita la parte blanca del centro de cada cuarto. Debes quedarte con la pulpa de lima.</p>

    <p>Coloca los trozos de lima en un vaso corto o taza resistente. Agrega el azúcar.</p>

    <p>Utiliza un mazo o mortero para aplastar las limas y el azúcar en el fondo del vaso. Este proceso libera
        los jugos y los aceites de la piel de la lima, que aportan sabor al cóctel.</p>

    <p>Agrega hielo al vaso. Añade la cachaça sobre el hielo.</p>

    <p>Remueve bien la mezcla para asegurarte de que el azúcar se disuelva y todos los ingredientes se mezclen.</p>

    <p>Decora el cóctel con una rodaja fina de lima en el borde del vaso y, opcionalmente,
        con unas rodajas de lima en el interior.</p>
</section>

