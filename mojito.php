<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

<p>2 oz (60 ml) de ron blanco.</p>
<p>1 oz (30 ml) de jugo de limón fresco.</p>
<p>2 cucharaditas de azúcar (ajusta según tu preferencia).</p>
<p>6-8 hojas de menta fresca.</p>
<p>Soda o agua con gas.</p>
<p>Hielo.</p>
<p>Rodaja de limón y ramita de menta para decorar.</p>

<h3>Pasos</h3>
    <p>En un vaso, coloca las hojas de menta y el azúcar. Utiliza un mortero o un mazo para aplastar suavemente las hojas de menta y liberar sus aceites esenciales.</p>

    <p>Agrega el jugo de limón y mezcla bien para disolver el azúcar.</p>

    <p>Llena el vaso con hielo.</p>

    <p>Vierte el ron sobre el hielo y revuelve.</p>

    <p>Completa el vaso con soda o agua con gas. Ajusta la cantidad según tu preferencia de fortaleza.</p>

    <p>Decora con una rodaja de limón y una ramita de menta fresca.</p>

</section>

