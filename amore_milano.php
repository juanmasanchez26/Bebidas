<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>1 y 1/2 oz (45 ml) de ginebra.</p>
    <p>1 oz (30 ml) de vermut rojo.</p>
    <p>1/2 oz (15 ml) de licor de naranja (como Cointreau o Triple Sec).</p>
    <p>1 toque de angostura bitters.</p>
    <p>Twist de naranja para decorar.</p>

    <h3>Pasos</h3>

    <p>Llena una coctelera con hielo para enfriarla.</p>

    <p>Añade la ginebra, el vermut rojo y el licor de naranja a la coctelera.</p>

    <p>Agrega un toque de angostura bitters para aportar complejidad.</p>

    <p>Agita enérgicamente durante unos 15-20 segundos para mezclar y enfriar los ingredientes.</p>

    <p>Cuela la mezcla en una copa de cóctel previamente enfriada.</p>

    <p>Exprime el twist de naranja sobre la bebida para liberar sus aceites esenciales, luego colócalo en el borde de la
        copa como decoración.</p>

