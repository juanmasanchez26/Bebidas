        tradicional.
    </p>

    <p>Una de las historias más famosas relacionadas con el Tom Collins es la "leyenda del falso Tom Collins". Se decía
        que un bromista entraba a un bar y afirmaba que alguien llamado Tom Collins estaba difamando a la gente, lo que
        incitaba a otros a buscar a Tom Collins enojados. Cuando llegaban al bar, se les servía un refrescante cóctel de
        gin con limón y soda, que más tarde se conoció como Tom Collins.</p>

    <p>El Tom Collins se convirtió en una bebida popular en el siglo XIX y ha seguido siendo una elección apreciada en
        la coctelería clásica. A lo largo de los años, ha habido variantes y adaptaciones, pero la versión clásica
        combina gin, jugo de limón, azúcar y soda, creando una bebida refrescante y equilibrada.</p>




