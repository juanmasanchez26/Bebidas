<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de gin.</p>
    <p>1 oz (30 ml) de jugo de limón fresco.</p>
    <p>1/2 oz (15 ml) de jarabe simple (azúcar disuelto en agua).</p>
    <p>Soda (agua carbonatada) al gusto.</p>
    <p>Rodaja de limón y/o cereza al marrasquino para decorar (opcional).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un vaso alto (collins) con hielo.</p>

    <p>Vierte el gin, el jugo de limón y el jarabe simple en el vaso.</p>

    <p>Remueve para mezclar los ingredientes.</p>

    <p>Completa el vaso con soda al gusto.</p>

    <p>Decora con una rodaja de limón y/o una cereza al marrasquino si lo deseas.</p>

</section>

