<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de vodka.</p>
    <p>4 oz (120 ml) de ginger beer (asegúrate de usar una marca de buena calidad).</p>
    <p>1/2 oz (15 ml) de jugo de lima fresco.</p>
    <p>Rodaja de lima y hojas de menta para decorar (opcional).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena una taza de cobre (taza Moscow Mule) con hielo.</p>

    <p>Vierte el vodka y el jugo de lima sobre el hielo.</p>

    <p>Completa con ginger beer al gusto.</p>

    <p>Remueve suavemente para mezclar los ingredientes.</p>

    <p>Decora con una rodaja de lima y hojas de menta si lo deseas.</p>

