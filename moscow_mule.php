<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>El Moscow Mule se originó en Los Ángeles, California, en la década de 1940. La historia comienza con tres
        individuos: John G. Martin, presidente de una destilería llamada Heublein; Jack Morgan, propietario del Cock 'n'
        Bull, un pub en Los Ángeles; y Rudolph Kunett, dueño de una empresa de ginger beer llamada Pier 1 Imports.
    </p>

    <p>En ese momento, Martin estaba tratando de promocionar su vodka Smirnoff, que no estaba teniendo mucho éxito en el
        mercado estadounidense. Morgan, por otro lado, tenía un exceso de ginger beer que había creado en su pub, el
        Cock 'n' Bull. Kunett también estaba buscando promover su ginger beer. Los tres hombres se unieron para crear el
        Moscow Mule como un medio para promocionar sus productos.
    </p>

    <p>El Moscow Mule se sirvió en una taza de cobre, que se convirtió en un distintivo del cóctel y ayudó a
        promocionarlo. Se decía que la taza de cobre mantendría la bebida extraordinariamente fría, lo que la hacía
        especialmente refrescante.</p>

    <p>El nombre "Moscow Mule" hace referencia al vodka (Moscow por la procedencia rusa del vodka) y al ginger beer
        (Mule, haciendo referencia al carácter fuerte y picante de la bebida). La elección del nombre contribuyó a la
        leyenda y al éxito del cóctel.</p>

    <p>El Moscow Mule se volvió increíblemente popular y ayudó a establecer la reputación de la vodka en los Estados
        Unidos. Además, impulsó la popularidad del ginger beer. El cóctel sigue siendo un favorito en la coctelería y se
        sirve en todo el mundo.</p>




