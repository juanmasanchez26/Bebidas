<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>3 oz (90 ml) de vino tinto español (preferiblemente un vino joven y ligero).</p>
    <p>3 oz (90 ml) de gaseosa o refresco de limón.</p>
    <p>Rodajas de limón y/o naranja (opcional).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena una copa grande o un vaso de balon con cubos de hielo para enfriarla.</p>

    <p>Vierte el vino tinto en la copa.</p>

    <p>Añade la gaseosa o refresco de limón sobre el vino tinto.</p>

    <p>Remueve suavemente con una cuchara larga para mezclar los ingredientes.</p>

    <p>Si lo prefieres, decora con rodajas de limón y/o naranja.</p>

</section>

