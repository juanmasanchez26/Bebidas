<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>El French 75 se originó durante la Primera Guerra Mundial en Francia, específicamente en París. Se cree que fue
        creado en el famoso Harry's New York Bar, un lugar de reunión para soldados estadounidenses y británicos en la
        década de 1910. El cóctel originalmente se llamaba "Soixante-Quinze", que es francés para "setenta y cinco", en
        referencia al cañón militar M1897 de 75 mm utilizado por el ejército francés.
    </p>

    <p>Este rápidamente se convirtió en una bebida asociada con la elegancia y la sofisticación, lo que lo
        convirtió en una elección popular entre la alta sociedad parisina. La mezcla de ginebra, limón y azúcar se
        combinaba con el efervescente champán, creando un cóctel que capturaba la esencia del glamour de la Belle
        Époque.
    </p>

    <p>El nombre proviene del cañón M1897 de 75 mm, que era conocido por su poder y precisión en el campo de
        batalla. La bebida adquirió este nombre debido a su impacto refrescante y chispeante. A lo largo de los años, el
        cóctel ha mantenido su legado y sigue siendo un símbolo de sofisticación.
    </p>

    <p>El French 75 ha resistido la prueba del tiempo y sigue siendo una bebida apreciada en la coctelería. Su
        equilibrio de sabores y el toque efervescente del champán lo hacen perfecto para brindar en celebraciones
        especiales o disfrutar en cualquier ocasión que requiera un toque de elegancia.

    </p>


</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>1 y 1/2 oz (45 ml) de ginebra.</p>
    <p>3/4 oz (22 ml) de jugo de limón fresco.</p>
    <p>1/2 oz (15 ml) de jarabe simple (azúcar y agua en partes iguales).</p>
    <p>2 oz (60 ml) de champán o vino espumoso.</p>
    <p>Hielo</p>

    <h3>Pasos</h3>

    <p>En una coctelera, agita la ginebra, el jugo de limón y el jarabe simple con hielo hasta que estén bien enfriados.
    </p>

    <p>Cuela la mezcla en una copa flauta (copa alargada) previamente enfriada.</p>

    <p>Completa con el champán o el vino espumoso</p>

    <p>Decora con una rodaja de limón o una cáscara de limón.</p>

</section>

