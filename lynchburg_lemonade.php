        <h3>Pasos</h3>

        <p>Vierte 1 y 1/2 oz de Jack Daniel's Tennessee Whiskey sobre el hielo.</p>

        <p>Agrega 1 oz de triple sec (licor de naranja).</p>

        <p>Exprime el jugo de un limón o pomelo fresco y agrégalo al vaso.</p>

        <p>Completa con Sprite o 7UP para llenar el vaso.</p>

        <p>Remueve suavemente para mezclar los ingredientes.</p>

        <p>Decora con una rodaja de limón o pomelo en el borde del vaso o dentro del cóctel.</p>

