    <h2>Historia</h2>
    <p> El Pisco Sour es objeto de una disputa amigable entre Perú y Chile, ya que ambos países reclaman
        ser su lugar de
        origen. Ambos países tienen una larga tradición de producción de pisco, un aguardiente de uva, y afirman haber
        inventado este refrescante cóctel. Aunque el origen exacto es motivo de debate, lo que es indiscutible es que el
        Pisco Sour ha sido una parte esencial de la cultura de ambos países durante décadas.
    </p>
    <p>Origen en Perú: Una de las historias más aceptadas sobre el origen del Pisco Sour lo sitúa en el bar Morris en Lima, Perú, a
        principios del siglo XX. Según esta versión, el bartender estadounidense Victor Vaughen Morris habría creado el
        cóctel mezclando pisco, jugo de limón, jarabe simple, hielo y clara de huevo. Este cóctel se convirtió en un
        favorito local y eventualmente se popularizó en todo el país.</p>

    <p>Origen en Chile: Chile también reclama la creación del Pisco Sour. En la ciudad de Iquique, en el norte de Chile,
        se asegura que
        un barman llamado Elliot Stubb habría inventado la bebida en 1920 en el Hotel Bolívar. La receta chilena del
        Pisco Sour es muy similar a la peruana, pero suele incluir un poco de amargo de Angostura.</p>



</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de pisco.</p>
    <p>1 oz (30 ml) de jugo de limón fresco.</p>
    <p>3/4 oz (22 ml) de jarabe simple (mezcla de azúcar y agua).</p>
    <p>1 clara de huevo.</p>
    <p>2-3 gotas de amargos de Angostura.</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena una coctelera con todos los ingredientes: pisco, jugo de limón fresco, jarabe simple, clara de huevo y
        hielo.</p>

    <p>Agita enérgicamente durante unos 15-20 segundos para mezclar y enfriar los ingredientes.</p>

    <p>Cuela la mezcla en una copa de cóctel previamente enfriada.</p>

    <p>Añade 2-3 gotas de amargos de Angostura en la superficie de la bebida.</p>

</section>

