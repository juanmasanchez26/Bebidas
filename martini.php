    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

<p>2 1/2 oz (75 ml) de ginebra o vodka.</p>
<p>1/2 oz (15 ml) de vermut seco (ajusta según tu preferencia).</p>
<p>1/2 oz (15 ml) de jarabe simple (azúcar disuelto en agua).</p>
<p>Aceituna o cáscara de limón para decorar (opcional).</p>
<p>Hielo.</p>

<h3>Pasos</h3>
    <p>Llena un vaso mezclador con hielo para enfriarlo.</p>

    <p>Agrega la ginebra o el vodka y el vermut seco en el vaso mezclador.</p>

    <p>Remueve la mezcla con una cuchara de cóctel durante unos 20-30 segundos para enfriarla y mezclar los
        ingredientes.</p>

    <p>Cuela la mezcla en una copa de Martini previamente enfriada.</p>

    <p>Decora con una aceituna en un palillo o una cáscara de limón, si lo deseas.</p>

