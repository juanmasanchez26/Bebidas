<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de limoncello.</p>
    <p>2 oz (60 ml) de vino espumoso (como prosecco o cava).</p>
    <p>1 oz (30 ml) de agua con gas.</p>
    <p>Rodaja de limón o twist de limón para decorar (opcional).</p>
    <p>Hojas de menta (opcional).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena una copa de vino o una copa de balón con hielo.</p>

    <p>Vierte el limoncello sobre el hielo.</p>

    <p>Agrega el vino espumoso.</p>

    <p>Completa con agua con gas al gusto.</p>

    <p>Remueve suavemente para mezclar los ingredientes.</p>

    <p>Decora con una rodaja de limón o un twist de limón y hojas de menta si lo deseas.</p>

</section>

