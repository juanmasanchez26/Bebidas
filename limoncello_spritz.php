    <h2>Historia</h2>
    <p>El limoncello es un licor de limón tradicional que se elabora en Italia, especialmente en la región de Campania,
        en la costa de Amalfi y en Sicilia. Se produce mediante la maceración de cáscaras de limón en alcohol, lo que le
        confiere un sabor y aroma intensos a limón.
    </p>

    <p>El Limoncello Spritz es una variante de los cócteles spritz que se han popularizado en Italia y en todo el mundo.
        El spritz tradicional combina vino espumoso, agua con gas y un licor amargo como el Aperol o el Campari. El
        Limoncello Spritz reemplaza el licor amargo por el dulce y aromático limoncello, lo que le da un carácter
        cítrico y refrescante.
    </p>

    <p>Los cócteles spritz son una parte importante de la cultura del aperitivo en Italia, donde la gente se reúne antes
        de cenar para disfrutar de bebidas y aperitivos ligeros. El Limoncello Spritz se ha convertido en una elección
        popular para aquellos que buscan un aperitivo fresco y sabroso.
    </p>

    <p>El Limoncello Spritz ha ganado popularidad en los últimos años y se ha convertido en una bebida de elección en
        bares y restaurantes, especialmente durante los meses más cálidos.</p>



