    <h2>Historia</h2>
    <p>El Grasshopper nació en la década de 1910 en el restaurante Tujague, ubicado en el French Quarter de Nueva
        Orleans. Se le atribuye al barman Philip Guichet, quien creó este cóctel dulce y cremoso. Aunque la fecha exacta
        de su invención puede variar, se ha convertido en un clásico de la coctelería.
    </p>

    <p>El Grasshopper se inspira en los sabores de la repostería, ya que combina crema de menta y crema de cacao, dos
        licores dulces y aromatizados. Su sabor suave y refrescante lo ha convertido en una opción popular como
        digestivo o postre líquido.
    </p>

    <p>El Grasshopper ganó popularidad en la década de 1950 y 1960, en parte debido a la tendencia de los cócteles
        dulces y cremosos en ese período. A menudo se le servía en cócteles o fiestas como una bebida refrescante y
        agradable.
    </p>

    <p>A lo largo de los años, el Grasshopper ha sido reinventado en diferentes formas, incluyendo versiones con helado
        y variaciones de sabores. Es conocido por su versatilidad y se puede disfrutar de muchas maneras diferentes.
    </p>


</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>3/4 oz (22 ml) de crema de menta (como Crème de Menthe verde).</p>
    <p>3/4 oz (22 ml) de crema de cacao (blanca o oscura).</p>
    <p>3/4 oz (22 ml) de crema líquida (nata líquida).</p>
    <p>Hielo</p>

    <h3>Pasos</h3>

    <p>Llena una coctelera con hielo.</p>

    <p>Agrega la crema de menta, la crema de cacao y la crema líquida a la coctelera.</p>

    <p>Agita enérgicamente durante unos 15-20 segundos para mezclar y enfriar los ingredientes.</p>

    <p>Cuela la mezcla en una copa de cóctel previamente enfriada.</p>

    <p>Puedes decorar con hojuelas de chocolate, si lo deseas.</p>

</section>

