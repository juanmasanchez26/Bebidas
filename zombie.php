<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>1 y 1/2 oz (45 ml) de ron blanco.</p>
    <p>1 y 1/2 oz (45 ml) de ron dorado.</p>
    <p>1 oz (30 ml) de ron de alta prueba (overproof rum)</p>
    <p>3/4 oz (22 ml) de jugo de limón fresco.</p>
    <p>1/2 oz (15 ml) de jugo de naranja.</p>
    <p>1/4 oz (7 ml) de jarabe de granadina.</p>
    <p>1/4 oz (7 ml) de falernum (licor aromatizado).</p>
    <p>2 dashes de bitter de angostura.</p>
    <p>Rodaja de naranja y cereza al marrasquino para decorar (opcional).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena una coctelera con hielo.</p>

    <p>Agrega todos los ingredientes, incluyendo los tres tipos de ron, el jugo de limón, el jugo de naranja, la
        granadina, el falernum y los bitter de angostura.</p>

    <p>Agita enérgicamente durante unos 15-20 segundos.</p>

    <p>Cuela la mezcla en un vaso tiki o en un vaso alto lleno de hielo.</p>

    <p>Decora con una rodaja de naranja y una cereza al marrasquino si lo deseas.</p>

</section>

