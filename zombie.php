    <h2>Historia</h2>
    <p>El Zombie fue creado por el bárman Donn Beach (Don the Beachcomber) en su restaurante homónimo en Hollywood en la
        década de 1930. Donn Beach fue uno de los pioneros en la creación de cócteles tiki y en la popularización de la
        cultura tiki en los Estados Unidos.
    </p>

    <p>El cóctel Zombie es un ejemplo clásico de los cócteles tiki, que se caracterizan por su exotismo y sus sabores
