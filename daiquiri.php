    <p>El Daiquirí es un cóctel clásico que se originó en Cuba a fines del siglo XIX. Su
        creación se atribuye a Jennings Cox, un ingeniero de minas estadounidense que trabajaba en la ciudad de
        Daiquirí, en la región oriental de Cuba. Se dice que Cox mezcló ron, jugo de limón y azúcar en una jarra con
        hielo, creando así el prototipo del Daiquirí original.</p>
    <p>La bebida ganó popularidad rápidamente, y su receta se fue refinando con el tiempo. El Daiquirí se convirtió en
        un cóctel emblemático de Cuba y un favorito entre los visitantes, incluyendo a figuras famosas como Ernest
        Hemingway, quien disfrutaba de esta bebida en el famoso bar El Floridita de La Habana.</p>
    

