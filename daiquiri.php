<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

<p>2 oz (60 ml) de ron blanco.</p>
<p>1 oz (30 ml) de jugo de limón fresco.</p>
<p>1/2 oz (15 ml) de jarabe simple (azúcar disuelto en agua).</p>
<p>Jugo de fruta(Por ejemplo: Frutilla)</p>
<p>Hielo.</p>

<h3>Pasos</h3>
    <p>Llena una coctelera con hielo.</p>

    <p>Agrega el ron, el jugo de limón, el jugo de fruta y el jarabe simple a la coctelera.</p>

    <p>Agita enérgicamente durante unos 10-15 segundos para enfriar y mezclar los ingredientes.</p>

    <p>Cuela la mezcla en una copa de cóctel previamente enfriada.</p>

    <p>Decora con una rodaja de limón y una ramita de menta fresca.</p>
    
    <p>Decora con una cereza al marrasquino (opcional).</p>

</section>

