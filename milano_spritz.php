<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de ginebra.</p>
    <p>1 oz (30 ml) de vermut rojo.</p>
    <p>Soda o agua con gas.</p>
    <p>Rodajas de naranja para decorar.</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena una copa de vino o vaso alto con hielo.</p>

    <p>Agrega la ginebra y el vermut rojo sobre el hielo.</p>

    <p>Remueve suavemente con una cuchara de bar para enfriar los ingredientes.</p>

    <p>Completa la bebida con soda o agua con gas, ajustando la cantidad según tu preferencia de efervescencia.</p>

    <p>Decora con rodajas de naranja para aportar frescura y un toque cítrico.</p>

    <p>Remueve ligeramente nuevamente para asegurar una mezcla uniforme.</p>

</section>

