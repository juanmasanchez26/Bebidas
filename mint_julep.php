<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>
        La historia del Mint Julep tiene sus raíces en la cultura sureña de Estados Unidos y se remonta al siglo XVIII.
        El término "julep" probablemente proviene de la palabra persa "golab," que significa agua de rosas. Los juleps
        originales eran, de hecho, mezclas medicinales que contenían agua de rosas.
    </p>

    <p>A lo largo del tiempo, la receta evolucionó y se adaptó, y en el sur de los Estados Unidos, especialmente en
        Kentucky, el Mint Julep se convirtió en una bebida emblemática. A medida que se popularizó, la mezcla cambió
        para incluir bourbon, un licor que tenía fuertes raíces en la región.</p>

    <p>Durante el siglo XIX, el Mint Julep se asoció cada vez más con la hospitalidad sureña y se convirtió en una
        bebida tradicionalmente disfrutada en reuniones al aire libre y eventos sociales. La mezcla de bourbon, menta y
        azúcar se convirtió en un símbolo de la vida elegante y relajada del sur de Estados Unidos.</p>

    <p>La conexión del Mint Julep con el Kentucky Derby se estableció de manera más formal en 1938, cuando comenzó a
        servirse en tazas de plata grabadas conmemorativas en el Derby. Esta tradición se ha mantenido desde entonces, y
        cada año se sirven miles de Mint Juleps durante el evento.</p>

    <p>En la actualidad, el Mint Julep no solo es apreciado en el sur de Estados Unidos, sino que se ha convertido en un
        cóctel clásico conocido a nivel internacional. Su frescura, combinación de sabores y su asociación con la
        elegancia sureña le han otorgado un estatus especial en el mundo de la coctelería.</p>


</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de bourbon.</p>
    <p>8-10 hojas de menta fresca.</p>
    <p>1 cucharadita de azúcar (opcional).</p>
    <p>Ramita de menta para decorar.</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Coloca las hojas de menta en el fondo de un vaso de julep o un vaso bajo.</p>

    <p>Añade el azúcar si lo deseas. Tradicionalmente, se utiliza azúcar en la mezcla, pero algunos prefieren omitirlo o
        ajustar la cantidad según el gusto.</p>

    <p>Con un mortero o con la parte posterior de una cuchara, presiona suavemente las hojas de menta y el azúcar para
        liberar los aceites esenciales de la menta.</p>

    <p>Agrega los cubitos de hielo al vaso.</p>

    <p>Decora con una ramita de menta fresca en la parte superior del vaso.</p>

    <p>Sirve con una pajita corta.</p>

