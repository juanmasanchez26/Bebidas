<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>El Whiskey Sour tiene sus raíces en los cócteles que se servían en la marina de los Estados Unidos en el siglo
        XIX. En ese período, se sabe que se consumían cócteles con limón y azúcar para combatir el escorbuto, una
        enfermedad causada por la deficiencia de vitamina C.
    </p>

    <p>A medida que los cócteles se popularizaron en tierra firme, el Whiskey Sour evolucionó y se convirtió en una
        bebida más refinada. Se dejó de utilizar el limón entero y se empezó a emplear el jugo de limón recién
        exprimido, lo que mejoró significativamente el sabor del cóctel.
    </p>

    <p>A lo largo de los años, el Whiskey Sour ha tenido diferentes variaciones y recetas, pero la versión clásica
        combina whisky, jugo de limón fresco y azúcar simple. Es un ejemplo de cóctel equilibrado que combina la fuerza
        del whisky con la acidez del limón y la dulzura del azúcar.</p>

    <p>El Whiskey Sour ha mantenido su popularidad a lo largo de las décadas y se ha convertido en un clásico atemporal
        en la coctelería. Su sabor equilibrado y su simplicidad en la preparación lo convierten en una elección
        apreciada por los amantes de los cócteles.</p>




</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de whisky (bourbon o whisky de centeno son opciones comunes).</p>
    <p>3/4 oz (22 ml) de jugo de limón fresco.</p>
    <p>1/2 oz (15 ml) de jarabe simple (azúcar disuelto en agua).</p>
    <p>Rodaja de limón y/o cereza al marrasquino para decorar (opcional).</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena una coctelera con hielo.</p>

    <p>Vierte el whisky, el jugo de limón y el jarabe simple en la coctelera.</p>

    <p>Agita enérgicamente durante unos 15-20 segundos para mezclar y enfriar los ingredientes.</p>

    <p>Cuela la mezcla en un vaso old-fashioned (vaso corto) con hielo fresco.</p>

    <p>Decora con una rodaja de limón y/o una cereza al marrasquino si lo deseas.</p>

</section>

