<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 y 1/2 oz (75 ml) de vodka de alta calidad.</p>
    <p>1/2 oz (15 ml) de vermut seco.</p>
    <p>Twist de limón o aceituna verde para decorar.</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un vaso mezclador con cubitos de hielo para enfriar el líquido y el vaso.</p>

    <p>Vierte el vermut seco en el vaso mezclador.</p>

    <p>Agita el vermut en el vaso para que cubra las paredes y luego deséchalo.</p>

    <p>Añade el vodka en el vaso mezclador sobre el hielo.</p>

    <p>Remueve suavemente durante unos 15-20 segundos para mezclar y enfriar los ingredientes.</p>

    <p>Cuela la mezcla en una copa de cóctel previamente enfriada.</p>
    
    <p>Decora con un twist de limón o una aceituna verde, según tu preferencia.</p>

