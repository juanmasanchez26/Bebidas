<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de whisky o bourbon de tu elección.</p>
    <p>3/4 oz (22 ml) de jarabe simple (azúcar y agua en partes iguales).</p>
    <p>1/2 limón, cortado en trozos.</p>
    <p>Varias hojas de menta fresca.</p>
    <p>Hielo</p>

    <h3>Pasos</h3>

    <p>En un vaso o coctelera, coloca los trozos de limón y las hojas de menta.</p>

    <p>Agrega el jarabe simple y utiliza un muddler o cuchara de madera para aplastar suavemente los ingredientes y
        liberar los aceites de la menta.</p>

    <p>Añade el whisky o bourbon y hielo al vaso o coctelera.</p>

    <p>Agita enérgicamente durante unos 15-20 segundos para enfriar y mezclar los ingredientes.</p>

    <p>Cuela la mezcla en un vaso bajo lleno de hielo fresco.</p>

    <p>Decora con una ramita de menta y una rodaja de limón.</p>

