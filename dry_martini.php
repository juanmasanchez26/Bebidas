<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 y 1/2 oz (75 ml) de ginebra de alta calidad.</p>
    <p>1/2 oz (15 ml) de vermut seco (opcionalmente, algunos prefieren una cantidad mínima o una simple "rinse" del
        vaso).</p>
    <p>Twist de limón o aceituna verde para decorar.</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un vaso mezclador con cubitos de hielo para enfriar el líquido y el vaso. Si prefieres un Dry Martini "más
        seco", puedes eliminar el hielo y simplemente enfriar el vaso.</p>

    <p>Vierte el vermut seco en el vaso mezclador. Gira el vaso para que el vermut cubra las paredes del vaso y luego
        desecha el exceso.</p>

    <p>Añade la ginebra en el vaso mezclador sobre el hielo o en el vaso enfriado sin hielo.</p>

    <p>Remueve suavemente con una cuchara de coctelería durante unos 15-20 segundos.</p>

    <p>Cuela la mezcla en una copa de cóctel previamente enfriada (copa Martini).</p>

    <p>Decora con un twist de limón exprimiendo una fina tira de cáscara de limón sobre el cóctel o con una aceituna
        verde, según tu preferencia.</p>

</section>

