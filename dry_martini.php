    <h2>Historia</h2>
    <p>El Dry Martini ha sido objeto de disputas sobre su origen a lo largo de los años. Se cree que se inspira en
        cócteles más antiguos, como el "Martinez", que eran una combinación de ginebra y vermut. Algunos relatos
        sugieren que se originó en el bar Knickerbocker Hotel de Nueva York a mediados del siglo XIX, mientras que otros
        argumentan que su verdadero origen está en California. Lo que es seguro es que el Martini ha evolucionado a lo
        largo del tiempo y ha llegado a simbolizar la elegancia en la coctelería.
    </p>

    <p>A medida que el cóctel ganó popularidad, se produjeron variaciones en la receta. Originalmente, se preparaba con
        ginebra y vermut dulce. Sin embargo, a lo largo del tiempo, la receta se simplificó y se convirtió en el "Dry
        Martini", que usa ginebra y vermut seco en una proporción que puede variar según las preferencias individuales.
        La tendencia hacia la sequedad del Martini se popularizó en la época de la Ley Seca en los Estados Unidos.
    </p>

    <p>El Martini se convirtió en un símbolo de elegancia y sofisticación en la cultura de la coctelería. Durante la
        época dorada de Hollywood, era la bebida favorita de muchas estrellas de cine y personalidades famosas, y su
        presencia en películas y series de televisión contribuyó a su estatus icónico.
    </p>

    <p>A pesar de su origen controvertido y su evolución a lo largo de los años, el Dry Martini sigue siendo uno de los
