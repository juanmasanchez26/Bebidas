<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>La historia de la Margarita es un poco controvertida, ya que existen varias teorías sobre su origen.</p>
    <p>Origen Mexicano: Una teoría popular sugiere que la Margarita se originó en México,
        posiblemente en Tijuana o Acapulco, en la década de 1930 o 1940. Se cree que fue
        creada por un bartender que buscaba una bebida refrescante y con un sabor equilibrado
        de agave, cítricos y dulzura. La Margarita toma su nombre de la palabra española
        "margarita", y se refiere a la flor que se utiliza
        como adorno en el cóctel.</p>
    <p>Origen en Estados Unidos: Otra teoría sostiene que la Margarita
        fue inventada en Estados Unidos, en particular en Tijuana,
        California, durante la prohibición en la década de 1930.
        Según esta teoría, Carlos "Danny" Herrera, un bartender
        en el restaurante Rancho La Gloria, creó la Margarita
        para una clienta que era alérgica a todas las bebidas
        alcohólicas excepto el tequila. Herrera mezcló tequila,
        licor de naranja y jugo de limón, y sirvió la bebida con
        hielo y una orilla de sal. La clienta, Margarita Henkel,
        quedó encantada, y la bebida se convirtió en un éxito.</p>

</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

<p>2 oz (60 ml) de tequila.</p>
<p>1 oz (30 ml) de licor de naranja (como Cointreau o Triple Sec).</p>
<p>1 oz (30 ml) de jugo de limón fresco.</p>
<p>Sal (opcional) para escarchar el borde del vaso.</p>
<p>Rodaja de limón o lima para decorar (opcional).</p>
<p>Hielo.</p>

<h3>Pasos</h3>
    <p>Si deseas, escarcha el borde de un vaso tipo margarita pasándolo por jugo de limón y luego por sal.</p>

    <p>Llena el vaso con hielo.</p>

    <p>En una coctelera, agita el tequila, el licor de naranja y el jugo de limón con hielo.</p>

    <p>Cuela la mezcla en el vaso preparado con hielo.</p>

    <p>Decora con una rodaja de limón o lima, si lo deseas.</p>

</section>

