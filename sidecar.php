<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de coñac (o brandy).</p>
    <p>1 oz (30 ml) de licor de naranja (Cointreau o Triple Sec).</p>
    <p>3/4 oz (22 ml) de jugo de limón fresco.</p>
    <p>Azúcar para escarchar el borde del vaso (opcional).</p>
    <p>Tira de cáscara de limón para decorar.</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Si lo deseas, puedes escarchar el borde de una copa de cóctel con azúcar.</p>

    <p>Llena la coctelera con hielo.</p>

    <p>Vierte el coñac, el licor de naranja y el jugo de limón en la coctelera.</p>

    <p>Agita enérgicamente durante unos 15-20 segundos para mezclar y enfriar los ingredientes.</p>

    <p>Cuela la mezcla en la copa de cóctel.</p>

    <p>Decora con una tira de cáscara de limón.</p>

</section>

