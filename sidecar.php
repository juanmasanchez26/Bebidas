        Estados Unidos (1920-1933). La Prohibición llevó a la clandestinidad de la producción y consumo de alcohol en
        los Estados Unidos, pero los cócteles no dejaron de ser apreciados. Los bebedores estadounidenses se aventuraban
        a bares clandestinos y speakeasies para disfrutar de cócteles creativos y sabrosos.
    </p>

    <p>El Sidecar se convirtió en un símbolo de la moda y el glamour de la época, siendo disfrutado por la alta sociedad
