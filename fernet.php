<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>30% de Fernet Branca</p>
    <p>70% partes de Coca-Cola</p>
    <p>Hielo</p>
    <p>Rodajas de limón (opcional, para decorar)</p>

    <h3>Pasos</h3>

    <p>Llena un vaso alto con hielo hasta casi el borde.</p>

    <p>Vierte Fernet Branca sobre el hielo.</p>

    <p>Luego, agrega Coca-Cola al vaso, o la cantidad deseada según tu preferencia de sabor.</p>

    <p>Revuelve suavemente la mezcla para asegurarte de que los ingredientes se combinen de manera uniforme.</p>

    <p>Opcionalmente, decora el borde del vaso con una rodaja de limón para darle un toque adicional de frescura y presentación.</p>

