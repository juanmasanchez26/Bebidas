        en un aspecto clave de la industria.</p>

    <h3>Prohibición y después</h3>
    <p>En el siglo XX, algunos países, como Estados Unidos, experimentaron períodos de prohibición del alcohol. Esto
        llevó a la proliferación de destilación clandestina y la creación de cócteles para ocultar el sabor del licor
        ilegal. Después de la prohibición, surgieron nuevas tendencias en la coctelería y la producción de cerveza
        artesanal.</p>

    <h3>Tendencias modernas</h3>
