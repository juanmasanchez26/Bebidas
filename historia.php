    <h3>Inicios y primeros cócteles</h3>
    <p>Aunque es difícil determinar el origen exacto del cóctel, el término "cocktail" se utilizó por primera vez a
        principios del siglo XIX en Estados Unidos. Se cree que el cóctel surgió como una mezcla de licores con
        azúcares, bíteres y agua para mejorar el sabor. El famoso "Old Fashioned" se considera uno de los primeros
        cócteles clásicos.</p>

    <h3>Era dorada de la coctelería</h3>
    <p>La segunda mitad del siglo XIX y principios del XX se conoce como la "Era Dorada de la Coctelería". Durante este
        período, los bares de lujo y los mixólogos expertos ganaron popularidad. Bartenders como Jerry Thomas se
        destacaron por sus habilidades y crearon bebidas icónicas como el Martini y el Manhattan.</p>

    <h3>Prohibición y speakeasies</h3>
    <p>La prohibición en Estados Unidos (1920-1933) tuvo un impacto significativo en la coctelería. La demanda de
        licores ilegales llevó a la creación de speakeasies, bares clandestinos donde se servían cócteles innovadores
        para disfrazar el sabor del alcohol de baja calidad.</p>

    <h3>Tiki y exotismo</h3>
    <p>Después de la Segunda Guerra Mundial, surgieron los bares Tiki que ofrecían cócteles exóticos y coloridos,
        inspirados en la cultura polinesia. Bartenders como Donn Beach y Trader Vic popularizaron bebidas como el Mai
        Tai y el Zombie, creando una tendencia que perdura.</p>

    <h3>Revolución de la coctelería contemporánea</h3>
    <p>A finales del siglo XX y principios del XXI, hubo un renacimiento en la coctelería artesanal. Mixólogos
        comenzaron a experimentar con ingredientes frescos, jarabes caseros y técnicas innovadoras. La atención al
        detalle y la presentación se convirtieron en aspectos clave de la experiencia del cóctel.</p>

    <h3>Resurgimiento de los cócteles clásicos</h3>
    <p>A medida que crecía la apreciación por la coctelería clásica, muchos barmen comenzaron a redescubrir recetas
        antiguas y a honrar la artesanía tradicional. Los cócteles como el Negroni, el Daiquiri y el Sazerac
        experimentaron un resurgimiento en popularidad.</p>

    <h3>Tendencias contemporáneas</h3>
    <p>Hoy en día, la coctelería abarca desde el regreso de los cócteles de autor hasta la creación de bebidas más
        accesibles. La personalización y la experimentación continúan siendo tendencias importantes. La inclusión de
        ingredientes inusuales, infusiones, y presentaciones creativas son características distintivas de la escena de
        la coctelería actual.</p>

    <h3>Cócteles sin alcohol y saludables</h3>
    <p>En respuesta a la creciente conciencia sobre la salud, los cócteles sin alcohol y las bebidas bajas en alcohol
        han ganado popularidad. Mixólogos están explorando opciones que utilizan ingredientes frescos y técnicas
        creativas para ofrecer experiencias de cócteles sin la presencia del alcohol tradicional.</p>
