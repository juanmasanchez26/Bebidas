    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>2 oz (60 ml) de ron cubano (como Havana Club).</p>
    <p>Cola (como Coca-Cola o Pepsi) al gusto.</p>
    <p>Rodaja de lima o limón.</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena un vaso alto con hielo.</p>

    <p>Vierte el ron sobre el hielo.</p>

    <p>Completa el vaso con cola al gusto.</p>

    <p>Exprime el jugo de lima o limón sobre la bebida y, si lo deseas, coloca una rodaja de lima o limón en el vaso
        como decoración.</p>

    <p>Remueve suavemente para mezclar los ingredientes.</p>

