<section class="historia" id="historia">
    <h2>Historia</h2>
    <p>En la década de 1940, el Aeropuerto Foynes en Irlanda era una escala común para los vuelos transatlánticos.
        Debido a las condiciones climáticas impredecibles, los pasajeros a menudo tenían que esperar en el aeropuerto
        durante horas o incluso días. Joe Sheridan, un chef y barman en el restaurante del aeropuerto, buscó una forma
        de reconfortar a los viajeros en medio del frío y el cansancio
    </p>

    <p>Joe Sheridan creó el Irish Coffee como una bebida caliente y reconfortante. La receta original combina whisky
        irlandés, café caliente recién preparado, azúcar y nata batida. La nata batida se coloca en la parte superior
        del cóctel, creando una capa rica y suave que contrasta con el café y el whisky.
    </p>

    <p>El Irish Coffee se convirtió en un éxito instantáneo y se extendió más allá de Irlanda a medida que los viajeros
        compartían su experiencia con el cóctel. En 1952, un periodista estadounidense llamado Stanton Delaplane probó
        el Irish Coffee en el Aeropuerto Foynes y lo introdujo en el Buena Vista Café en San Francisco. El Buena Vista
        Café popularizó la bebida en los Estados Unidos, y desde entonces, el Irish Coffee ha sido un cóctel amado en
        todo el mundo.
    </p>

    <p>El Irish Coffee se ha convertido en un símbolo de hospitalidad irlandesa y es una bebida que combina la calidez
        del café con la suavidad del whisky y la riqueza de la nata batida. Se sirve en tazas o copas de vidrio caliente
        y es una elección popular para reconfortar en noches frías o después de una comida.</p>


</section>
<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>1 y 1/2 oz (45 ml) de whisky irlandés.</p>
    <p>4 oz (120 ml) de café caliente recién preparado.</p>
    <p>2 cucharaditas de azúcar moreno.</p>
    <p>Nata batida (crema espesa batida ligeramente).</p>

    <h3>Pasos</h3>

    <p>Prepara el café caliente y asegúrate de que la taza o copa esté caliente.</p>

    <p>Agrega el whisky irlandés y el azúcar moreno al café caliente y revuelve hasta que el azúcar se disuelva por
        completo.</p>

    <p>Con una cuchara, coloca la nata batida con cuidado sobre la superficie del café. Debe flotar en la parte
        superior.</p>

    <p>Sirve caliente y disfruta con una cuchara para mezclar la nata con el café a medida que lo bebes.</p>

</section>

