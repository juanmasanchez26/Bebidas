<section class="preparacion" id="preparacion">
    <h2>Proceso de Elaboración</h2>
    <h3>Ingredientes</h3>

    <p>1 y 1/2 oz (45 ml) de brandy.</p>
    <p>1 oz (30 ml) de crema espesa.</p>
    <p>1 oz (30 ml) de licor de cacao.</p>
    <p>Nuez moscada rallada para decorar.</p>
    <p>Hielo.</p>

    <h3>Pasos</h3>

    <p>Llena una coctelera con hielo.</p>

    <p>Agrega el brandy, la crema espesa y el licor de cacao en la coctelera.</p>

    <p>Agita enérgicamente durante unos 15-20 segundos para mezclar y enfriar los ingredientes.</p>

    <p>Cuela la mezcla en una copa de cóctel enfriada.</p>

    <p>Espolvorea una pizca de nuez moscada rallada en la parte superior como decoración.</p>

</section>

