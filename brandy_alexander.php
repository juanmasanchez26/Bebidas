    <h2>Historia</h2>
    <p>El Brandy Alexander se originó en los Estados Unidos a principios del siglo XX, durante la época conocida como la
        "Belle Époque". Esta fue una época de efervescencia cultural y creatividad en la coctelería.
    </p>

    <p>El Brandy Alexander es una variante del cóctel Alexander, que también se prepara con crema y un licor base, pero
        en el caso del Brandy Alexander, se utiliza brandy como licor base. El Alexander original se hacía con ginebra.
    </p>

    <p>El brandy es un licor que aporta un sabor suave y aterciopelado al cóctel, lo que lo convierte en una elección
        ideal para aquellos que disfrutan de bebidas cremosas y ligeramente dulces.
    </p>

    <p>El Brandy Alexander floreció en la época de la coctelería clásica, junto con otros cócteles icónicos como el
        Sidecar y el Martini. Fue ampliamente disfrutado en bares y salones de la época.</p>

    <p>A pesar de su origen en el pasado, el Brandy Alexander ha mantenido su popularidad a lo largo de los años y es
        considerado un cóctel clásico.</p>


